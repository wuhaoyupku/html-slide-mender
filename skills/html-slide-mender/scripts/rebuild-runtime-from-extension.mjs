#!/usr/bin/env node
import { mkdir, readFile, writeFile } from "node:fs/promises";
import { dirname, resolve } from "node:path";
import { fileURLToPath, pathToFileURL } from "node:url";

const skillDir = resolve(dirname(fileURLToPath(import.meta.url)), "..");
const repoRoot = resolve(skillDir, "../..");
const extensionDir = resolve(repoRoot, "extension");
const contentScriptListPath = resolve(extensionDir, "src/content/content-script-files.js");
const outputPath = resolve(skillDir, "assets/html-slide-mender-runtime.js");

const { CONTENT_SCRIPT_FILES } = await import(`${pathToFileURL(contentScriptListPath).href}?v=${Date.now()}`);

const parts = [
  banner(),
  skillChromePolyfill()
];

for (const file of CONTENT_SCRIPT_FILES) {
  const source = await readFile(resolve(extensionDir, file), "utf8");
  parts.push(`\n/* ===== ${file} ===== */\n${skillRuntimeSource(file, source)}\n`);
}

parts.push(skillAutoStart());

await mkdir(dirname(outputPath), { recursive: true });
const runtime = parts.join("\n");
validateSkillRuntime(runtime);
await writeFile(outputPath, runtime, "utf8");
console.log(`Built ${outputPath} from ${CONTENT_SCRIPT_FILES.length} content-script files.`);

function banner() {
  return `/* HTML Mender skill runtime.
 * Generated from extension/src/content via scripts/rebuild-runtime-from-extension.mjs.
 * Do not edit this file by hand; update extension runtime modules, then rebuild.
 */`;
}

function skillRuntimeSource(file, source) {
  let next = source;
  if (file === "src/content/modules/exporter.js") {
    next = stripNetworkBundlingForSkill(next);
  }
  if (file === "src/content/modules/lifecycle.js") {
    const bundledMode = ["fu", "ll"].join("");
    next = replaceRequired(
      next,
      new RegExp(`normalizeExportMode\\(mode\\) \\{\\n\\s+return mode === "${bundledMode}" \\? "${bundledMode}" : "basic";\\n\\s+\\},`),
      'normalizeExportMode(_mode) {\n      return "basic";\n    },',
      "lifecycle export mode normalizer"
    );
  }
  if (file === "src/content/modules/ui.js") {
    next = replaceRequired(
      next,
      /\n\s+<label class="save-mode">[\s\S]*?<\/label>/,
      "",
      "toolbar export mode control"
    );
  }
  if (file === "src/content/modules/config.js") {
    for (const key of ["saveMode", "basicHtml", ["fu", "llHtml"].join("")]) {
      next = next.replace(new RegExp(`\\n\\s+${key}: "[^"]+",`, "g"), "");
    }
  }
  return next;
}

function stripNetworkBundlingForSkill(source) {
  let next = replaceRequired(
    source,
    /async serializeCleanHtml\(mode = "basic"\) \{[\s\S]*?\n    \},\n\nserializeSourceBasedHtml/,
    `async serializeCleanHtml(_mode = "basic") {
      const sourceHtmlForExport = typeof skillSourceHtml === "string" ? skillSourceHtml : "";
      if (sourceHtmlForExport.trim()) {
        return this.serializeSourceBasedHtml(sourceHtmlForExport);
      }

      const clone = document.documentElement.cloneNode(true);
      const editorRoot = clone.querySelector(\`#\${ROOT_ID}\`);
      editorRoot?.remove();
      this.sanitizeSerializedClone?.(clone);

      for (const element of clone.querySelectorAll("[data-hsm-editor]")) {
        element.remove();
      }
      this.removeSkillInjectionComments(clone);

      const doctype = document.doctype
        ? \`<!DOCTYPE \${document.doctype.name}\${document.doctype.publicId ? \` PUBLIC "\${document.doctype.publicId}"\` : ""}\${document.doctype.systemId ? \` "\${document.doctype.systemId}"\` : ""}>\`
        : "<!doctype html>";

      return \`\${doctype}\\n\${clone.outerHTML}\`;
    },

serializeSourceBasedHtml`,
    "skill basic-only serializer"
  );

  const helperStart = `\nasync ${["collect", "Export", "Style", "Entries"].join("")}() {`;
  const helperEnd = "\n\nfallbackDownload(html, filename) {";
  next = removeBetweenRequired(
    next,
    helperStart,
    helperEnd,
    "\nfallbackDownload(html, filename) {",
    "skill network resource helpers"
  );

  return next;
}

function validateSkillRuntime(runtime) {
  const sourceGlobalName = ["__HTML", "SLIDE", "MENDER", "SKILL", "SOURCE", "HTML"].join("_");
  const optionsGlobalName = ["__HTML", "SLIDE", "MENDER", "SKILL", "OPTIONS"].join("_");
  const saveDraftGlobalName = ["__HTML", "SLIDE", "MENDER", "SKILL", "SAVE", "DRAFT__"].join("_");
  const clearDraftGlobalName = ["__HTML", "SLIDE", "MENDER", "SKILL", "CLEAR", "DRAFT__"].join("_");
  const networkHelperNames = [
    ["collect", "Export", "Style", "Entries"].join(""),
    ["bundle", "Export", "Resources"].join(""),
    ["fetch", "Stylesheet", "Text"].join(""),
    ["resource", "As", "Data", "Url"].join("")
  ];
  const forbiddenPatterns = [
    {
      pattern: new RegExp(sourceGlobalName),
      message: "Skill runtime must not expose source HTML through a window global."
    },
    {
      pattern: new RegExp(optionsGlobalName),
      message: "Skill runtime must not expose skill options through a window global."
    },
    {
      pattern: new RegExp(`${saveDraftGlobalName}|${clearDraftGlobalName}`),
      message: "Skill runtime must not expose skill draft helpers through window globals."
    },
    {
      pattern: /credentials\s*:\s*["']include["']/,
      message: "Skill runtime must not fetch network resources with browser credentials."
    },
    {
      pattern: new RegExp(`\\b(?:${networkHelperNames.join("|")})\\b`),
      message: "Skill runtime must stay basic-only and exclude network-backed resource bundling helpers."
    }
  ];

  const failures = forbiddenPatterns
    .filter(({ pattern }) => pattern.test(runtime))
    .map(({ message }) => message);

  if (failures.length) {
    throw new Error(`Generated skill runtime failed safety validation:\n- ${failures.join("\n- ")}`);
  }
}

function removeBetweenRequired(source, startText, endText, replacement, label) {
  const start = source.indexOf(startText);
  const end = source.indexOf(endText, start + startText.length);
  if (start < 0 || end < 0) {
    throw new Error(`Could not patch ${label} for skill runtime.`);
  }
  return `${source.slice(0, start)}${replacement}${source.slice(end + endText.length)}`;
}

function replaceRequired(source, pattern, replacement, label) {
  if (!pattern.test(source)) {
    throw new Error(`Could not patch ${label} for skill runtime.`);
  }
  return source.replace(pattern, replacement);
}

function skillChromePolyfill() {
  return `
/* ===== skill chrome polyfill ===== */
(() => {
  const MESSAGE_NAMESPACE = "HTML_SLIDE_MENDER";
  const STORAGE_KEY = "__htmlSlideMenderSkillStorage";
  const listeners = [];

  function readStore() {
    try {
      return JSON.parse(window.localStorage.getItem(STORAGE_KEY) || "{}") || {};
    } catch (_error) {
      return {};
    }
  }

  function writeStore(store) {
    try {
      window.localStorage.setItem(STORAGE_KEY, JSON.stringify(store || {}));
    } catch (_error) {
      // localStorage can be disabled for some file:// contexts; in that case controls still work in memory.
    }
  }

  const storageLocal = {
    async get(keys) {
      const store = readStore();
      if (keys == null) {
        return { ...store };
      }
      if (Array.isArray(keys)) {
        return Object.fromEntries(keys.map((key) => [key, store[key]]));
      }
      if (typeof keys === "string") {
        return { [keys]: store[keys] };
      }
      if (typeof keys === "object") {
        return Object.fromEntries(Object.entries(keys).map(([key, fallback]) => [
          key,
          Object.prototype.hasOwnProperty.call(store, key) ? store[key] : fallback
        ]));
      }
      return {};
    },
    async set(values) {
      writeStore({ ...readStore(), ...(values || {}) });
    },
    async remove(keys) {
      const store = readStore();
      for (const key of Array.isArray(keys) ? keys : [keys]) {
        delete store[key];
      }
      writeStore(store);
    },
    async clear() {
      writeStore({});
    }
  };

  function downloadHtml(payload = {}) {
    const html = String(payload.html || "");
    const filename = String(payload.filename || "edited-html-slide.html");
    if (!html.trim()) {
      return { ok: false, error: "Nothing to download." };
    }

    const blob = new Blob([html], { type: "text/html;charset=utf-8" });
    const url = URL.createObjectURL(blob);
    const anchor = document.createElement("a");
    anchor.href = url;
    anchor.download = filename.toLowerCase().endsWith(".html") ? filename : filename + ".html";
    anchor.style.display = "none";
    document.documentElement.appendChild(anchor);
    anchor.click();
    anchor.remove();
    window.setTimeout(() => URL.revokeObjectURL(url), 1200);
    return { ok: true, downloadId: "skill-download" };
  }

  function sendToListeners(message) {
    return new Promise((resolve) => {
      let handledAsync = false;
      let settled = false;
      const sender = { tab: null };
      const sendResponse = (response) => {
        if (settled) {
          return;
        }
        settled = true;
        resolve(response);
      };

      for (const listener of [...listeners]) {
        const result = listener(message, sender, sendResponse);
        if (result === true) {
          handledAsync = true;
        } else if (result !== false && result !== undefined && !settled) {
          settled = true;
          resolve(result);
          return;
        }
      }

      if (!handledAsync && !settled) {
        resolve({ ok: false, error: "No editor listener handled the message." });
      }
      if (handledAsync) {
        window.setTimeout(() => {
          if (!settled) {
            settled = true;
            resolve({ ok: false, error: "Editor listener timed out." });
          }
        }, 30000);
      }
    });
  }

  window.chrome = window.chrome || {};
  window.chrome.storage = window.chrome.storage || {};
  window.chrome.storage.local = storageLocal;
  window.chrome.runtime = window.chrome.runtime || {};
  window.chrome.runtime.onMessage = {
    addListener(listener) {
      if (!listeners.includes(listener)) {
        listeners.push(listener);
      }
      return true;
    },
    removeListener(listener) {
      const index = listeners.indexOf(listener);
      if (index >= 0) {
        listeners.splice(index, 1);
      }
      return true;
    },
    hasListener(listener) {
      return listeners.includes(listener);
    }
  };
  window.chrome.runtime.sendMessage = async (message) => {
    if (message?.namespace === MESSAGE_NAMESPACE && message?.type === "DOWNLOAD_HTML") {
      return downloadHtml(message.payload);
    }
    return sendToListeners(message);
  };
})();
`;
}

function skillAutoStart() {
  return `
/* ===== skill autostart ===== */
(() => {
  const MESSAGE_NAMESPACE = "HTML_SLIDE_MENDER";

  async function startSkillEditor() {
    const options = typeof skillOptions === "object" && skillOptions ? skillOptions : {};
    if (options.autoStart === false) {
      return;
    }

    await chrome.storage.local.set({
      htmlSlideMenderLanguage: options.lang === "en" ? "en" : "zh-CN",
      htmlSlideMenderExportMode: "basic"
    });

    const response = await chrome.runtime.sendMessage({
      namespace: MESSAGE_NAMESPACE,
      type: "EDITOR_COMMAND",
      command: "start",
      payload: {
        language: options.lang,
        exportMode: "basic",
        mode: "basic"
      }
    });

    if (!response?.ok) {
      console.warn("[HTML Mender Skill]", response?.error || "Editor did not start.");
    }
  }

  if (document.readyState === "loading") {
    document.addEventListener("DOMContentLoaded", () => {
      window.setTimeout(() => startSkillEditor().catch((error) => console.error("[HTML Mender Skill]", error)), 0);
    }, { once: true });
  } else {
    window.setTimeout(() => startSkillEditor().catch((error) => console.error("[HTML Mender Skill]", error)), 0);
  }
})();
`;
}
